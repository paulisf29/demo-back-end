package com.example.demo;

public class AirplaneModel {
    String make;
    int model;

    public AirplaneModel(String make, int model) {
        this.make = make;
        this.model = model;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public int getModel() {
        return model;
    }

    public void setModel(int model) {
        this.model = model;
    }

    

    
}
